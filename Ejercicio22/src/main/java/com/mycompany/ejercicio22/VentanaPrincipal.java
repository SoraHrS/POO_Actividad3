    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        String nombre;
        double salario, horasTrabajadas, salarioMensual;
        
        nombre = txtIngresoNombre.getText();
        horasTrabajadas = Double.parseDouble(txtHoras.getText());
        salario = Double.parseDouble(txtSalario.getText());
        
        Empleado empleado = new Empleado(nombre, salario, horasTrabajadas);
        
        empleado.calcularSalarioMensual();
        
        salarioMensual = empleado.salarioMensual;
        
        if (salarioMensual > 450000) {
            txtMostrarNombre.setText(nombre);
            txtSalarioMensual.setText(String.valueOf(salarioMensual));
        }else{
            txtMostrarNombre.setText(nombre);
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

